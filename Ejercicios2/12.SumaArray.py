#Escribe un programa que devuelva la suma de los valores del array anterior.
array = [4,5,75,2,4,0,23]

# Inicializar una variable para almacenar la suma
suma = 0

# Recorrer el array y sumar
for valor in array:
    suma += valor

# Mostrar la suma de los valores
print("La suma de los valores en el array es:", suma)