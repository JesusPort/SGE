#Escribe un programa que devuelva la media del array anterior.
# El array con los valores
array = [43,2,6,4,23,1]

# Calcular la suma de los valores en el array
suma = sum(array)

# Calcular la cantidad de elementos en el array
cantidad = len(array)

# Calcular la media
media = suma / cantidad

# Mostrar la media
print("La media de los valores en el array es:", media)